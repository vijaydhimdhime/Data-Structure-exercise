arr = [10, 20, 30, 40, 50]
print("befor insertion:", arr)
arr.insert(2, 25)  # Insert 25 at index 2
print("after insertion:", arr)
arr.insert(0, 18)    # Insert 18 at the beginning
print("after 18 insertion:", arr)
arr.insert(0, 6)    # Insert 6 at the beginning
print("after 6 insertion:", arr)
arr.insert(5, 60)  # Insert 60 at index 5
print("after 60 insertion:", arr)
arr.append(70)  # Append 70 to the end of the array
print("after 70 insertion:", arr)


